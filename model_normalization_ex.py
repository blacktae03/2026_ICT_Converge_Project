import numpy as np

# 1. 가상의 센서 데이터 (샘플 3개)
# Feature 1 (왼쪽 열): 0 ~ 5V 범위
# Feature 2 (오른쪽 열): 0 ~ 3.3V 범위
X_raw = np.array([
    [5.0, 3.3],   # 둘 다 최대치 (Max)
    [2.5, 1.65],  # 둘 다 절반 (Half)
    [0.0, 0.0],   # 둘 다 최소치 (Min)
    # [5.2, -0.1]   # 노이즈 발생 상황 (범위 초과)
])

# ---------------------------------------------------------
# [핵심] 각 센서의 "최대 전압"을 순서대로 적어줍니다.
# ---------------------------------------------------------
# 왼쪽 열(x1)은 5.0으로, 오른쪽 열(x2)은 3.3으로 나눌 준비
max_voltages = np.array([5.0, 3.3]) 

# 2. 안전장치 (Clipping): 노이즈로 튀는 값들을 강제로 0~Max 사이로 자름
# x1은 0~5로, x2는 0~3.3으로 알아서 잘립니다.
# (None은 최솟값을 0으로 고정한다는 뜻)
X_clipped = np.clip(X_raw, 0.0, max_voltages)

# 3. 정규화 (Normalization)
# Numpy가 알아서 [열1 / 5.0], [열2 / 3.3]을 계산합니다.
X_norm = X_clipped / max_voltages

print("--- 원본 데이터 ---")
print(X_raw)
print("\n--- 정규화된 데이터 (0 ~ 1) ---")
print(X_norm)